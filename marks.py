m = input("Введіть свою кількість балів (від 0 до 100): ")
m = int(m)

if m <= 49:
    print("Ваша оцінка: незадовільно")
elif 50 <= m <= 69:
    print("Ваша оцінка: задовільно")
elif 70 <= m <= 89:
    print("Ваша оцінка: добре")
elif 90 <= m <= 100:
    print("Ваша оцінка: відмінно")
else:
    print("Некоректне значення. Введіть число від 0 до 100.")
